package com.t4complexNumber;

public class ComplexeNumber {
    protected double realNum;
    protected double imaginaryNum;

    public ComplexeNumber(double realNum, double imaginaryNum) {
        this.realNum = realNum;
        this.imaginaryNum = imaginaryNum;
    }

    public double getRealNum() {
        return realNum;
    }
    public double getImaginaryNum() {
        return imaginaryNum;
    }


}
